package controller;

import DAO.ProductDAO;
import DTO.ProductDTO;
import java.io.IOException;
import java.io.PrintWriter;
import java.util.List;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

/**
 *
 * @author ASUS
 */
public class DeleteProductServlet extends HttpServlet {

    private static final long serialVersionUID = 1L;

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        try ( PrintWriter out = response.getWriter()) {
            /* TODO output your page here. You may use following sample code. */
            // Get the product ID parameter from the request
            String productId = request.getParameter("productId");

            // Create a ProductDTO object to store the product information
            ProductDTO product = new ProductDTO();
            product.setProductId(productId);

            // Call the DAO method to delete the product
            ProductDAO productDAO = new ProductDAO();
            boolean success = productDAO.deleteProduct(product);

            if (success) {
                // Product deletion successful
                // Get the updated product list
                List<ProductDTO> productList = productDAO.getAllProducts();

                // Set productList as a request attribute
                request.setAttribute("products", productList);

                // Set a success message as a request attribute
                request.setAttribute("deleteSuccessMessage", "Product deleted successfully.");
            } else {
                // Product deletion failed
                // Set an error message as a request attribute
                request.setAttribute("deleteErrorMessage", "Failed to delete the product.");
            }

            // Forward the request to the showProducts.jsp or another appropriate page
            request.getRequestDispatcher("storePage.jsp").forward(request, response);
        }
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
